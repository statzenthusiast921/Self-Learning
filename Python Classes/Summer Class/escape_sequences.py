#ESCAPE SEQUENCES


#allows you to print out stuff that
#wouldn't normally get printed out
print(" \" ")
#prints out quotation mark

print("Hello\nWorld")

#\n separates first chunk and second chunk,
#prints first chunk out on first line
#second chunk gets printed out on second line


print("Hello\t\t\t\tWorld")
#tabs the second chunk over 4 times


#allows the use of a single quotation
print("Hello\'World")
print("\'Hello, World\'")

#no space in between
print("Hello\aWorld")
