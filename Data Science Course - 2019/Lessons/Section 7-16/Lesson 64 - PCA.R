#Lesson 64: Principal Component Analysis

#PCA

#Unsupervised machine learning method

#dimension reduction method

#objective of PCA is to summarize correlation among set of observed variables
#with a smaller set of linear combinations

#PCA is usually only used for analysis

#output is the principal components

#1st principal component (PC1) captures highest variance and 2nd covers
#the second highest and so on

#variables may have loading on more than 1 PC but they are represented
#by principal components with strong relation and not weaker ones

#if all variables are unique, data dimension reduction is not possible

#PCs with eigen values greater than 1 are good candidates for retentions


